# Get user input for base and height of the triangle
base = int(input('Please enter the length of the base: '))
height = int(input('Please enter the height: '))

# Calculate the area using the triangle area formula (1/2 * base * height)
area = int((base*height)/2)

# Display the result
print('The area of the triangle is,',area, 'm^2')